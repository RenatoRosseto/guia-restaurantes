<?php	
	function soma_valores ($x, $y)
	{
		$resultado = $x + $y;	
		return $resultado;
	}
	
	function subtrai_valores ($x, $y)
	{
		$resultado = $x - $y;	
		return $resultado;
	}
	
	function multiplica_valores ($x, $y)
	{
		$resultado = $x * $y;	
		return $resultado;
	}
	
	function divide_valores ($x, $y)
	{
		$resultado = $x / $y;		
		return $resultado;
	}	
?>